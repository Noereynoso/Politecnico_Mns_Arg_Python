#while es una estructura de control que permite ejecutar un bloque de
#código repetidamente mientras se cumpla una condición específica. 
#Básicamente, el bloque de código se ejecutará una y otra vez hasta que la 
#condición dada sea evaluada como falsa. A continuación un concepto 
#más detallado sobre el bucle while en Python: 

contador= 0

while contador <= 60:
    print("El valor del contador es:", contador)
    contador += 1 # Incrementa el contador en 1 en cada iteración
    print("Bucle while terminado")

