#Crear una función que eleve un número al cuadrado y lo imprima
#funcion
def elevar_al_cuadrado(numero):
    return numero **2
#ejemplo de uso
resultado = elevar_al_cuadrado(5)    
print(f"el cuadrado de 5 es  {resultado}")
