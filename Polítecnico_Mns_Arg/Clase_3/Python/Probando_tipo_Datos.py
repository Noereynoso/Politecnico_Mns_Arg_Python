# Números Enteros
numero_entero = 42 # Una variable que almacena un número entero
print(numero_entero) # Un Output: 42
print(type(numero_entero)) # Output: <class 'int'>

# Numeros Flotantes
numero_flotante = 3.14 # Una variable que almacena un número flotante
print(numero_flotante) # Un Output: 3.14
print(type(numero_flotante)) # Output: <class 'float'>

# Cadenas de caracteres
cadena= "Hola, Mundo" # Es una variable que almacena un string
print(cadena) # Un Output: Hola, Mundo
print(type(cadena)) # Output: <class 'str'>

# Litas Elementos
lista = [1, 2, 3, 4, 5] # Es una variable que almacena una list
print(lista) # Un Output: [1,2,3,4,5]
print(type(lista)) # Output: <class 'list'>

# Tuplas inmutables
tupla = (1, 2, 3, 4, 5) #es una variable que almacena una tuple
print (tupla) # Un Output: (1, 2, 3, 4, 5)
print(type(tupla)) # Output: <class 'tuple'>

# Diccionario clave-valor
diccionario = {"nombre": "juan", "edad": "21"} # Es una variable que almacena un dict
print(diccionario) # Un Output: {'nombre': 'juan', 'edad': '21'}
print(type(diccionario)) # Output: <class 'dict'>

# Valores booleanos
valor_booleano = True # Es una variable que almacena un bool
print(valor_booleano) # Un Output: True
print(type(valor_booleano)) # Output: <class 'bool'>