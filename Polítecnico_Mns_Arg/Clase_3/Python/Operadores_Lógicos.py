# Operador AND
condicional1 = False
condicional2 = True
resultado = condicional1 and condicional2 # Operador lógico AND
print(resultado) # Un Output: False

# Operador OR

condicional3 = True
condicional4 = False
resultado2 = condicional3 or condicional4 # Operador lógico OR
print(resultado2) # Un Output: True

# Operador NOT
condicional5 = True
resultado3 = not condicional5 # Operador lógico NOT
print(resultado3) # Un Output: False